package com.nan.t;

public class Person {
    private String name;
    private int i;
    public Person(String name,int i){
        this.name = name;
        this.i = i;
    }
    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
    public int getI() {
        return i;
    }
    public void setI(int i) {
        this.i = i;
    }
}
