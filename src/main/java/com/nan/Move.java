package com.nan;

public class Move {

    public static void main(String[] args) {
        System.out.println(20 >> 2);
        System.out.println((-20) >> 2);
        System.out.println(20 >>> 2);
        System.out.println((-20) >>> 2);
        
        System.out.println(Integer.toBinaryString(1));
        System.out.println(Integer.toBinaryString(-1));
        System.out.println((-1) << 29);
        System.out.println(Integer.toBinaryString(-536870912));
        System.out.println(Integer.toBinaryString(536870912));
        
        
    }

}
